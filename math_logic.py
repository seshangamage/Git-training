def sum(a,b):
    sum = a+b
    return sum

def multiply(a,b):
    multiply = a*b
    return multiply

def divide(a,b):
    divide = a/b
    return divide

def find_max(a,b):
    if a>b:
        return a
    else:
        return b
    
def find_min(a,b):
    if a<b:
        return a
    else:
        return b
